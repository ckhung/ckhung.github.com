    <h2 class="sec">貴哥不用 PowerPoint</h2>

    <p>簡報還在用 PowerPoint? <a href=
    "http://blog.ofset.org/ckhung/index.php?post/115a">
    雲端運算只是口號</a>? 貴哥從 2006 年開始， 就一直用
    html + css + javascript 製作 <a href="../s/">舊的簡報</a>、
    <a href= "http://newtoypia.blogspot.tw/2011/01/blog-post.html">
    把簡報放在雲端上</a>。 事實上我從來就不曾學會用 PowerPoint 做簡報。 
    採用 presentacular 之類的技術做簡報， 有很多好處： 檔案輕巧、
    開放格式、 任何瀏覽器可播放； 但在美觀上面比較抱歉。
    Prezi 很炫, 但採用 flash; 2011 年時發現
    <a href="http://blog.ofset.org/ckhung/index.php?post/118a">
    jessyink + graphviz</a> 也很炫, 而且又開放, 但略嫌複雜。
    2012 年發現 <a href=
    "http://sozi.baierouge.fr/wiki/en:welcome">sozi</a>,
    不僅跟 Prezi 一樣炫, 而且中文沒問題、 使用很簡單, 完全符合貴哥的需求,
    決定以後都來改用 sozi, 把它當做心智圖跟簡報的綜合體。
    [也有網友推薦另一個文字簡報工具 <a href=
    "http://lab.hakim.se/reveal-js/#/">reveal.js</a>]</p>

    <h2 class="sec" id="play">播放說明</h2>

    <p>Sozi 跟 jessyink 一樣都是 inkscape 的外掛;
    所產生的簡報檔只不過就是一張內嵌了 javascript
    的 .svg 圖檔而已。 所以任何夠新、
    支援標準的瀏覽器都可以播放 sozi 所做的簡報。
    依據個人的經驗， firefox 對 svg 支援最齊全。
    以下是從 <a href=
    "http://sozi.baierouge.fr/wiki/en:control">官網</a>
    抄過來的播放說明。 用瀏覽器打開簡報檔之後:</p>

    <ol>
      <li>下一頁: 滑鼠左鍵、 Page Down、 右方向鍵、
      下方向鍵(無換場效果)、 空間棒、 Enter</li>

      <li>上一頁: 滑鼠右鍵、 Page Up、 左方向鍵、
      下方向鍵(無換場效果)</li>

      <li>第一頁: Home</li>

      <li>最後一頁: End</li>

      <li>所有幻燈片清單: 滑鼠中間鍵、 t、 T</li>
    </ol>

    <p>也可以擺脫固定的頁面模式,
    直接在整張畫布上移動與縮放:</p>

    <ol>
      <li>移動: 用滑鼠拖畫面</li>

      <li>放大縮小: 滑鼠滾輪、 +/-</li>

      <li>左右旋轉: shift+滑鼠滾輪、 r/R</li>

      <li>回到目前那一頁: =</li>

      <li>觀看整張畫布: f, F</li>
    </ol>

