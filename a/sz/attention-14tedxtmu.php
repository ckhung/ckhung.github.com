    我在網路上分享一篇教學文還有很多範例。
    以上當然都是免費分享； 因為我能夠合理期待獲得回報的，
    不是金錢贊助而是各位在 FB 或 g+ 或噗浪上分享我的文章，
    提升我的能見度 :-) 也謝謝大家剛剛對我付出十幾分鐘的注意力。</p>
